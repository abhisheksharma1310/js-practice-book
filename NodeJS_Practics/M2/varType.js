//variable var
var varValue = "Hello Var";
console.log(varValue);
varValue = "Hi Var";
console.log(varValue);
{
    var varValue = "Hello Var!";
    console.log(varValue);
    varValue = "Hi Var!";
    console.log(varValue);
}
console.log(varValue);

